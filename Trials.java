public class Trials{

public static void main(String[] args){


int start=1;
int value=1;

for(start=1;start<=4;start++)
{
	for(value=1;value<=start;value++)
{		 
		System.out.print(value + " ");
}
		System.out.println("");
}

for(start=1;start<=4;start++)
{
for(value=1;value<=start;value++)
{
System.out.print("*");
}
System.out.println("");
}

}
}